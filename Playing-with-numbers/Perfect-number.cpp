// O(sqrt(n))

int _perfect_Number(int _number)
{
	int _square_Of_Number = (int) sqrt(_number);
	int _sum = 1;

	for(int _factors = 2; _factors <= _square_Of_Number; _factors++)
	{
		if(_number % _factors == 0)
		{
			_sum += _factors;
			_sum += (_number / _factors);
		}
	}
	
	return (_sum == _number);
}

